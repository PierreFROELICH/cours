DATE_SORTIE_FILM DATE,
PAYS_SORTIE_FILM CHAR(3));

CREATE TABLE CINEMA.FILMS_CATEGORIE
(
ID_FILM INT,
ID_CATEGORIE INT
)
;

CREATE TABLE CINEMA.CATEGORIE
(
ID_CATEGORIE INT,
LIB_CATEGORIE VARCHAR(200)
)
;

CREATE TABLE CINEMA.EVALUATIONS
(
ID_FILM INT,
ID_CRITIQUE INT,
NOMBRE_ETOILES DECIMAL(10,1),
NOMBRE_EVALUATIONS INT
)
;

CREATE TABLE CINEMA.CRITIQUE
(
ID_CRITIQUE INT,
NOM_CRITIQUE VARCHAR(200)
)
;

